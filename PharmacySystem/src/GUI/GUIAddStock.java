    /**
     * *
     * Save Stock
     */
    private void Add() {
        String Message = "";
        int value = MessageManager.YesOrNoMessage(null, null);

        //Set Stock Data
        Stock stock = new Stock();
        stock.setItemName(txtItemName.getText().trim());
        stock.setResalePrice(ResalePrice);
        try {
            stock.setQuantity(Integer.parseInt(txtQuantity.getText().trim()));
        } catch (NumberFormatException ex) {
            Message = "Invalid Quantity";
        }

        // Set Stock Batch Data
        StockBatch stockBatch = new StockBatch();
        stockBatch.setItemName(txtItemName.getText().trim());
        stockBatch.setBatch(txtItemBatchNo.getText().trim());
        try {
            stockBatch.setEXPDate(txtExpDate.getText().trim());
        } catch (NumberFormatException ex) {
            Message = "Invalid Exp Date";
        }

        try {
            stockBatch.setCostPrice(Float.parseFloat(txtCostPrice.getText().trim()));
        } catch (NumberFormatException ex) {
            Message = "Invalid Cost Price";
        }

        try {
            stockBatch.setQuantity(Integer.parseInt(txtQuantity.getText().trim()));
        } catch (NumberFormatException ex) {
            Message = "Invalid Quantity";
        }

        //Check Message
        if (value == 0) {

            try {
                // Meneger to Parse Data
                StockManager.SaveorUpdate(stock, stockBatch);
                Cancel();
            } catch (nullCreateUserException ex) {
                Message = "Invalid Create User";
            } catch (nullCreateDateException ex) {
                Message = "Invalid Create Date";
            } catch (nullCreateTimeException ex) {
                Message = "Invalid  Create Time";
            } catch (nullItemNameException ex) {
                Message = "Invalid  Item Name";
            } catch (nullBatchNoException ex) {
                Message = "Invalid Item Batch No";
            } catch (nullCostPriceException ex) {
                Message = "Invalid Cost Price";
            } catch (nullExpDateException ex) {
                Message = "Invalid  Exp Date";
            } catch (nullQuantityException ex) {
                Message = "Invalid Quantity";
            } catch (DatabaseException ex) {
                Message = "Database Error";
            } catch (nullResalpriceException ex) {
                Message = "Invalid Resale Price";
            }
        }
        lblMassage.setText(Message);
    }

