    /**
     * Enter Barcord And Get Data
     *
     * @param Key
     */
    private void BarcordEnter(int Key) {
        String Message = "";
        if (Key == KeyEvent.VK_ENTER) {
            try {
                StockManager.Select(txtItemBarcode.getText().trim(), this);
            } catch (NullPointerException ex) {
                Message = "Invalid Barcode";
            }
        }
        lblMassage.setText(Message);
    }

